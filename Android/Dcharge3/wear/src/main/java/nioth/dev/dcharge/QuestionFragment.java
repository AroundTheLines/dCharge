        View view = inflater.inflate(R.layout.question_activity, container, false);



        CircularButton confirmButton = (CircularButton) view.findViewById(R.id.yesButton);
        CircularButton rejectButton = (CircularButton) view.findViewById(R.id.noButton);

        mTextView = (TextView) view.findViewById(R.id.QuestionText);
        mTextView.setText(values.get(counter));

        counter++;
        confirmButton.setOnClickListener(this);
        rejectButton.setOnClickListener(this);

